function sum(a, b) {
  return a + b;
}

function subtraction(a, b) {
  return a - b;
}

function multiplication(a ,b) {
  return a * b;
}

function division(a, b) {
  return a / b;
}

function module(a, b) {
  return a % b;
}

console.log(sum(15,15));
console.log(subtraction(60, 45));
